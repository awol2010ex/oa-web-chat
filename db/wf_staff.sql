CREATE TABLE
    wf_staff
    (
        STAFFID VARCHAR(32) NOT NULL,
        LOGINNAME VARCHAR(64),
        PASSWORD VARCHAR(64),
        STAFFNAME VARCHAR(64),
        OWNERUNITID VARCHAR(32),
        UNITID VARCHAR(32),
        KIND VARCHAR(10),
        ADMINLEVEL VARCHAR(100),
        STATUS CHAR(1) DEFAULT '1' NOT NULL,
        IDNUMBER CHAR(18),
        BIRTHDAY CHAR(10),
        SEX CHAR(1),
        TEL VARCHAR(50),
        EMAIL VARCHAR(128),
        ADDRESS VARCHAR(128),
        EXT1 VARCHAR(32),
        EXT2 VARCHAR(32),
        EXT3 VARCHAR(32),
        EXT4 VARCHAR(32),
        EXT5 VARCHAR(32),
        EXT6 VARCHAR(32),
        EXT7 VARCHAR(32),
        EXT8 VARCHAR(32),
        EXT9 VARCHAR(32),
        CREATORID VARCHAR(32),
        CREATEDTIME CHAR(19),
        LASTMODIFIERID VARCHAR(32),
        LASTMODIFIEDTIME CHAR(19),
        DELFLAG CHAR(1) DEFAULT 'n' NOT NULL,
        PRIMARY KEY (STAFFID)
    )
    ENGINE=InnoDB DEFAULT CHARSET=utf8